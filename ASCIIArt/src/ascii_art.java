
public class ascii_art {
	public static void main(String[] args) {
		System.out.println("This is an image of a cherry about to be placed on top of two ice cream cones.");
		cherry();
		iceCream();
	}	
	public static void cherry() {
		System.out.println("                       __.--~~.,-.__\\");
		System.out.println("                      `~-._.-(`-.__`-.");
		System.out.println("                             \\    `~~`\\");
		System.out.println("                        .--./ \\");
		System.out.println("                       /#   \\  \\.--.\\");
		System.out.println("                       \\    /  /#   \\");
		System.out.println("                        '--'   \\    /\\");
		System.out.println("                                '--'\\");
	}
	public static void iceCream() {
		System.out.println("______________________________________________");
		System.out.println("______________/       \\__________/       \\__");
		System.out.println("_____________/         \\________/         \\");
		System.out.println("____________{           }______{           }");
		System.out.println("_____________@@@@@@@@@@@________@@@@@@@@@@@");
		System.out.println("______________@@@@@@@@@__________@@@@@@@@@");
		System.out.println("_______________@@@@@@@____________@@@@@@@");
		System.out.println("________________@@@@@______________@@@@@");
		System.out.println("_________________@@@________________@@@");
		System.out.println("__________________@__________________@");
	}
		
}
